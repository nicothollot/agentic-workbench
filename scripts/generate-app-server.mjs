import { mkdir, writeFile } from "node:fs/promises";
import { spawn } from "node:child_process";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const outDir = path.resolve(__dirname, "../src/generated/app-server");

const runCodex = async (args, stdio = "pipe") =>
  await new Promise((resolve, reject) => {
    const child = spawn("codex", args, { stdio });
    let stdout = "";
    let stderr = "";

    if (child.stdout) {
      child.stdout.setEncoding("utf8");
      child.stdout.on("data", (chunk) => {
        stdout += chunk;
      });
    }
    if (child.stderr) {
      child.stderr.setEncoding("utf8");
      child.stderr.on("data", (chunk) => {
        stderr += chunk;
      });
    }

    child.on("exit", (code) => {
      if (code === 0) {
        resolve({ stdout, stderr });
        return;
      }
      reject(new Error(`codex ${args.join(" ")} exited with code ${code ?? -1}`));
    });
    child.on("error", reject);
  });

await mkdir(outDir, { recursive: true });

await runCodex(["app-server", "generate-ts", "--experimental", "--out", outDir], "inherit");

const versionOutput = await runCodex(["--version"]);
const generatedCodexCliVersion = /\b(\d+\.\d+\.\d+(?:[-+][0-9A-Za-z.-]+)?)\b/.exec(
  `${versionOutput.stdout}\n${versionOutput.stderr}`
)?.[1];

if (!generatedCodexCliVersion) {
  throw new Error("Could not determine the Codex CLI version used to generate app-server types.");
}

await writeFile(
  path.join(outDir, "protocolVersion.ts"),
  [
    "// Generated by scripts/generate-app-server.mjs.",
    "// Keep this version in sync with the Codex CLI that produced the app-server protocol types.",
    `export const GENERATED_CODEX_CLI_VERSION = "${generatedCodexCliVersion}";`,
    "export type protocolVersion = { generatedCodexCliVersion: typeof GENERATED_CODEX_CLI_VERSION };",
    ""
  ].join("\n")
);
